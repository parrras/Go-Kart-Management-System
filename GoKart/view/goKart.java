    private void loadListToTable(List<model1> kartList) {
        DefaultTableModel model = (DefaultTableModel) racerTBL.getModel();

        // Clear existing rows if needed
        model.setRowCount(0);

        // Populate the table with kart data
        kartList.forEach(kart -> model.addRow(new Object[]{
            kart.getFullName(),
            kart.getage(),
            kart.getgender(),
            kart.getContact(),
            kart.getkartNo()
        }));
    }


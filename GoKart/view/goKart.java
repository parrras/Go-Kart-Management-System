    private void btnDELETEActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDELETEActionPerformed
        try {
            int kartNo = Integer.parseInt(txtFLDkartno.getText().trim());
            boolean found = false;

            // Iterate through the list to find the racer with the specified Kart No
            for (int i = 0; i < kartList.size(); i++) {
                if (kartList.get(i).getkartNo() == kartNo) {
                    // Remove the racer from the list
                    kartList.remove(i);
                    found = true;

                    // Update the table
                    loadListToTable(kartList);

                    // Clear the form
                    clearRacerForm();

                    // Show success message
                    showDialogBox("Racer deleted successfully.", "Success", JOptionPane.INFORMATION_MESSAGE);
                    break;
                }
            }

            if (!found) {
                // Show error if the Kart No is not found
                showDialogBox("Kart No not found. Please enter a valid Kart No.", "Error", JOptionPane.WARNING_MESSAGE);
            }
        } catch (NumberFormatException e) {
            // Show error if Kart No is invalid
            showDialogBox("Please enter a valid Kart No.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnDELETEActionPerformed

    private void btnUPDATEActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUPDATEActionPerformed
        boolean isValid = true;

        // Validate Kart Number (100-199) for the racer to update
        isValid &= validateField(
                txtFLDkartno, "Kart Number", LBLerrorkartno,
                "Kart Number must be between 100 and 199.",
                errorColor, neonGreenColor,
                util1.isValidKartNo(txtFLDkartno.getText().trim())
        );

        if (!isValid) {
            // Show error if Kart Number is invalid
            showDialogBox("Please enter a valid Kart Number to update.", "Invalid Input", JOptionPane.ERROR_MESSAGE);
            return;
        }

        int kartNo = Integer.parseInt(txtFLDkartno.getText().trim()); // Parse Kart Number
        boolean racerFound = false;

        for (model1 kart : kartList) {
            if (kart.getkartNo() == kartNo) {
                // Racer found, validate other fields before updating

                // Validate Full Name (max 4 words)
                isValid &= validateField(
                        txtFLDfullname, "Full Name", LBLerrorfullname,
                        "Full Name must not exceed 4 words.",
                        errorColor, neonGreenColor,
                        util1.isValidName(txtFLDfullname.getText().trim())
                        && txtFLDfullname.getText().trim().split("\\s+").length <= 4
                );

                // Validate Age (18-30)
                isValid &= validateField(
                        txtFLDage, "Age", LBLerrorage,
                        "Age must be between 18 and 30.",
                        errorColor, neonGreenColor,
                        util1.isValidAge(txtFLDage.getText().trim())
                );

                // Validate Gender (Male or Female)
                isValid &= validateField(
                        txtFLDgender, "Gender", LBLerrorgender,
                        "Gender must be Male or Female.",
                        errorColor, neonGreenColor,
                        util1.isValidGender(txtFLDgender.getText().trim())
                );

                // Validate Contact (Starts with 9, 10 digits)
                isValid &= validateField(
                        txtFLDcontact, "Contact", LBLerrorcontact,
                        "Contact must start with 9 and have exactly 10 digits.",
                        errorColor, neonGreenColor,
                        util1.isValidContact(txtFLDcontact.getText().trim())
                );

                if (isValid) {
                    // Update racer's details if all validations pass
                    kart.setFullName(txtFLDfullname.getText().trim());
                    kart.setage(Short.parseShort(txtFLDage.getText().trim()));
                    kart.setgender(txtFLDgender.getText().trim());
                    kart.setContact(txtFLDcontact.getText().trim());

                    racerFound = true;
                    break;
                } else {
                    // Stop further execution if validation fails
                    showDialogBox("Validation failed. Racer details not updated.", "Update Failed", JOptionPane.ERROR_MESSAGE);
                    return;
                }
            }
        }

        if (racerFound) {
            // Reload the table to show updated information
            loadListToTable(kartList);

            // Display success message
            showDialogBox("Racer information updated successfully.", "Success", JOptionPane.INFORMATION_MESSAGE);

            // Clear input fields
            clearRacerForm();
        } else {
            // Racer with the given Kart Number not found
            showDialogBox("No"
                    + "+ racer found with Kart Number: " + kartNo, "Update Failed", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnUPDATEActionPerformed

    private void btnADDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnADDActionPerformed
        boolean isValid = true;

        // Validate Full Name (max 4 words)
        isValid &= validateField(
                txtFLDfullname, "Full Name", LBLerrorfullname,
                "Full Name must not exceed 4 words.",
                errorColor, neonGreenColor,
                util1.isValidName(txtFLDfullname.getText().trim())
                && txtFLDfullname.getText().trim().split("\\s+").length <= 4
        );

        // Validate Age (18-30)
        isValid &= validateField(
                txtFLDage, "Age", LBLerrorage,
                "Age must be between 18 and 30.",
                errorColor, neonGreenColor,
                util1.isValidAge(txtFLDage.getText().trim())
        );

        // Validate Gender (Male or Female)
        isValid &= validateField(
                txtFLDgender, "Gender", LBLerrorgender,
                "Gender must be Male or Female.",
                errorColor, neonGreenColor,
                util1.isValidGender(txtFLDgender.getText().trim())
        );

        // Validate Contact (Starts with 9, 10 digits)
        isValid &= validateField(
                txtFLDcontact, "Contact", LBLerrorcontact,
                "Contact must start with 9 and have exactly 10 digits.",
                errorColor, neonGreenColor,
                util1.isValidContact(txtFLDcontact.getText().trim())
        );

        // Validate Kart Number (100-199)
        isValid &= validateField(
                txtFLDkartno, "Kart Number", LBLerrorkartno,
                "Kart Number must be between 100 and 199.",
                errorColor, neonGreenColor,
                util1.isValidKartNo(txtFLDkartno.getText().trim())
        );

        if (isValid) {
            // Create a new racer object
            model1 newRacer = new model1(
                    txtFLDfullname.getText().trim(),
                    Short.parseShort(txtFLDage.getText().trim()),
                    txtFLDgender.getText().trim(),
                    txtFLDcontact.getText().trim(),
                    Integer.parseInt(txtFLDkartno.getText().trim())
            );

            // Check if Kart Number already exists
            if (checkDuplicateRacer(newRacer)) {
                txtFLDkartno.setBorder(createTitledBorder(errorColor, "Kart No"));
                showDialogBox("Kart Number already exists.", "Duplicate Entry", JOptionPane.WARNING_MESSAGE);
            } else {
                // Add the racer to the list
                kartList.add(newRacer);
                clearRacerForm();
                loadListToTable(kartList);
                txtFLDkartno.setBorder(createTitledBorder(neonGreenColor, "Kart No"));
                showDialogBox("Racer added successfully.", "Success", JOptionPane.INFORMATION_MESSAGE);
            }
        }


    }//GEN-LAST:event_btnADDActionPerformed

    // Helper method to check for duplicate racers
    private boolean checkDuplicateRacer(model1 racer) {
        return kartList.stream().anyMatch(existing -> existing.getkartNo() == racer.getkartNo());
    }

    private boolean validateField(JTextField textField, String fieldName, JLabel errorLbl, String errorMsg, Color errorColor, Color successColor, boolean isValidFormat) {
        if (util1.isNullOrEmpty(textField.getText())) {
            textField.setBorder(createTitledBorder(errorColor, fieldName));
            errorLbl.setText("Field cannot be empty!");
            errorLbl.setVisible(true);
            return false;
        } else if (!isValidFormat) {
            textField.setBorder(createTitledBorder(errorColor, fieldName));
            errorLbl.setText(errorMsg);
            errorLbl.setVisible(true);
            return false;
        } else {
            textField.setBorder(createTitledBorder(successColor, fieldName));
            errorLbl.setVisible(false);
            return true;
        }
    }


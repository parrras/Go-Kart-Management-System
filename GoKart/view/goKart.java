    /**
     * Method to add kart data and populate the table.
     */
    private void registerKart(model1 kart) {
    // Get the table model
    DefaultTableModel model = (DefaultTableModel) racerTBL.getModel();
    
    // Clear existing rows to avoid gaps or empty rows
    model.setRowCount(0);

    // Add all rows from kartList to the table
    for (model1 k : kartList) {
        model.addRow(new Object[]{
            k.getFullName(), k.getage(), k.getgender(), k.getContact(), k.getkartNo()
        });
    }
    
    // Add the new kart to the list and table
    kartList.add(kart);
    model.addRow(new Object[]{
        kart.getFullName(), kart.getage(), kart.getgender(), kart.getContact(), kart.getkartNo()
    });
}


